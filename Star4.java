package practice;
import java.util.Scanner;
/**
 *
 * @author fathi
 */
public class Star4 {
    public static void main(String[] args){
        Scanner scan=new Scanner(System.in);
        int n;
        n=scan.nextInt();
        for(int i=1;i<=n;i++){
            for(int j=i;j<=n;j++){
                System.out.print(" ");
            }
            for(int k=1;k<=i;k++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
    
}
